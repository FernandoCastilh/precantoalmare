    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            final String BANCO = "jdbc:mysql://localhost/pousada"; //porta 3307 no IFRJ
            final String USUARIO = "root";
            final String SENHA = "";
            Connection conexao = DriverManager.getConnection(BANCO, USUARIO, SENHA);
            String sql = "INSERT INTO quarto (num_quarto, camas, armarios, frigobar, diaria, obs) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement comando = conexao.prepareStatement(sql);
            comando.setString(1, txtQuarto.getValue().toString());
            comando.setString(2, txtCama.getValue().toString());
            comando.setString(3, txtArmarios.getValue().toString());
            comando.setString(4, txtFrigobar.getValue().toString());
            comando.setString(5, txtDiaria.getText());
            comando.setString(6, txtObs.getText());

            int r = comando.executeUpdate();
            if(r==1){
                JOptionPane.showMessageDialog(null, "Cadastro efetuado!");
                txtQuarto.setValue("0");
                txtCama.setValue("0");
                txtArmarios.setValue("0");
                txtFrigobar.setValue("0");
                txtDiaria.setText("");
                txtObs.setText("");

            }
            else
            JOptionPane.showMessageDialog(null, "Erro! Cadastro não realizado!");
        }catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, "Erro de conexão!"+ e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

