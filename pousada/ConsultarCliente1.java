    private void tabCliMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabCliMouseClicked
        String codigo = tabCli.getValueAt(tabCli.getSelectedRow(), 0).toString();
        System.out.println("código cliente: "+codigo);
        try {
            final String BANCO = "jdbc:mysql://localhost/pousada";
            final String USUARIO = "root";
            final String SENHA = "";

            Connection conexao = DriverManager.getConnection(BANCO, USUARIO, SENHA);
            PreparedStatement comando = conexao.prepareStatement("Select * from cliente where codigo_cliente = ?");
            comando.setString(1, codigo);
            ResultSet resultado = comando.executeQuery();
            if (resultado != null && resultado.next()) {
                txtNome.setText(resultado.getString("nome"));
                txtCel.setText(resultado.getString("celular"));
                txtProf.setText(resultado.getString("profissao"));
                txtNacio.setText(resultado.getString("nacionalidade"));
                txtNasc.setText(resultado.getString("data_nasc"));
                txtRg.setText(resultado.getString("identidade"));
                txtCpf.setText(resultado.getString("cpf"));
                txtEmail.setText(resultado.getString("email"));
                txtEndereco.setText(resultado.getString("endereco"));
                txtUf.setText(resultado.getString("uf"));
                txtPais.setText(resultado.getString("pais"));
                txtCdd.setText(resultado.getString("cdd"));
                if(resultado.getString("genero").equals("M")){
                    rbMasculino.setSelected(true);
                }else if(resultado.getString("genero").equals("F"))
                {
                    rbFeminino.setSelected(true);
                }
            }
            resultado.close();
        } catch (SQLException ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(null, "Verifique a conexão com o banco de dados."+ex, "Erro!!!", 2);
        }
    }//GEN-LAST:event_tabCliMouseClicked

