    private void txtPesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesquisaKeyReleased
        try {
            final String BANCO = "jdbc:mysql://localhost/pousada";
            final String USUARIO = "root";
            final String SENHA = "";
            Connection conexao = DriverManager.getConnection(BANCO, USUARIO, SENHA);
            PreparedStatement comando = null;
            if(compara==1){
                comando = conexao.prepareStatement("Select * from cliente where nome like ? order by nome");
                comando.setString(1, txtPesquisa.getText() + "%");
            }
            else if(compara==2){
                comando = conexao.prepareStatement("Select * from cliente where identidade like ? order by nome");
                comando.setString(1, txtPesquisa.getText() + "%");
            }

            ResultSet resultado = comando.executeQuery();
            DefaultTableModel model = (DefaultTableModel) tabCli.getModel();
            model.setNumRows(0);
            while (resultado.next()) {
                model.addRow(new Object[]{resultado.getString("codigo_cliente"), resultado.getString("nome"), resultado.getString("identidade"),});
            }
            resultado.close();
        } catch (SQLException ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(null, "Verifique a conexão com o banco de dados.", "Erro!!!", 2);
        }
    }//GEN-LAST:event_txtPesquisaKeyReleased

