    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try {    
        final String BANCO = "jdbc:mysql://localhost/pousada";
            final String USUARIO = "root";
            final String SENHA = "";
            
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy"); 
            LocalDate dataInicio = LocalDate.parse(dataI.getText(), formato);
            LocalDate dataFim = LocalDate.parse(dataF.getText(), formato);

            Connection conexao = DriverManager.getConnection(BANCO, USUARIO, SENHA);
            PreparedStatement comando = null;
                comando = conexao.prepareStatement("select cod_quarto, sum(reserva) as valor from reserva where reserva.check_in >='" + dataInicio +  "' and reserva.check_in < '" + dataFim + "' and reserva.check_out > '" + dataInicio + "' and reserva.check_out <= '" + dataFim + "' group by cod_quarto order by reserva.cod_quarto");

            ResultSet resultado = comando.executeQuery();
            DefaultTableModel model = (DefaultTableModel) tabCli.getModel();
            model.setNumRows(0);
            Double vTotal=0.0;
            while (resultado.next()) {
                model.addRow(new Object[]{resultado.getString("cod_quarto"), "R$" + resultado.getString("valor"),});
               vTotal += resultado.getDouble("valor");
            }
            total.setText("R$" + vTotal.toString());
            resultado.close();
        } catch (SQLException ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(null, "Verifique a conex√£o com o banco de dados." + ex, "Erro!!!", 2);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

